/// True if `pubkey` is authorised for this slot -- either the current binding
/// or any pubkey that has previously paired with the slot secret.
pub fn slot_authorizes(slot: &ConnectSlot, pubkey: &str) -> bool {
    slot.current_pubkey.as_deref() == Some(pubkey)
        || slot.authorized_pubkeys.iter().any(|p| p == pubkey)
}

/// Authorise `pubkey` on a slot: make it the current binding and remember it in
/// `authorized_pubkeys`. The outgoing `current_pubkey` is preserved in the set,
/// so an earlier device stays auto-approved instead of being evicted. The set
/// is de-duplicated and FIFO-capped at [`MAX_AUTHORIZED_PUBKEYS`].
pub fn authorize_pubkey_on_slot(slot: &mut ConnectSlot, pubkey: &str) {
    if let Some(prev) = slot.current_pubkey.take() {
        if prev != pubkey {
            push_authorized(slot, prev);
        }
    }
    push_authorized(slot, pubkey.to_string());
    slot.current_pubkey = Some(pubkey.to_string());
}

/// Add a pubkey to a slot's authorised set (de-duplicated, FIFO-capped).
fn push_authorized(slot: &mut ConnectSlot, pubkey: String) {
    if slot.authorized_pubkeys.iter().any(|p| *p == pubkey) {
        return;
    }
    while slot.authorized_pubkeys.len() >= MAX_AUTHORIZED_PUBKEYS {
        slot.authorized_pubkeys.remove(0);
    }
    slot.authorized_pubkeys.push(pubkey);
}

/// Find a slot that authorises this client pubkey (current binding or set).
pub fn find_slot_by_pubkey<'a>(slots: &'a [ConnectSlot], pubkey: &str) -> Option<&'a ConnectSlot> {
    slots.iter().find(|s| slot_authorizes(s, pubkey))
}

/// Find a mutable slot that authorises this client pubkey (current binding or set).
pub fn find_slot_by_pubkey_mut<'a>(slots: &'a mut [ConnectSlot], pubkey: &str) -> Option<&'a mut ConnectSlot> {
    slots.iter_mut().find(|s| slot_authorizes(s, pubkey))
}

/// Find a slot by its secret using constant-time comparison.
/// Always iterates all slots to prevent timing-based slot identification.
/// Returns the first matching slot (lowest index).
pub fn find_slot_by_secret<'a>(slots: &'a [ConnectSlot], secret: &str) -> Option<&'a ConnectSlot> {
    let mut matched: Option<usize> = None;
    for (i, slot) in slots.iter().enumerate() {
        if constant_time_eq_str(&slot.secret, secret) && matched.is_none() {
            matched = Some(i);
        }
    }
    matched.map(|i| &slots[i])
}

/// Find a mutable slot by its secret using constant-time comparison.
/// Always iterates all slots to prevent timing-based slot identification.
/// Returns the first matching slot (lowest index).
pub fn find_slot_by_secret_mut<'a>(slots: &'a mut [ConnectSlot], secret: &str) -> Option<&'a mut ConnectSlot> {
    let mut matched: Option<usize> = None;
    for (i, slot) in slots.iter().enumerate() {
        if constant_time_eq_str(&slot.secret, secret) && matched.is_none() {
            matched = Some(i);
        }
    }
    matched.map(move |i| &mut slots[i])
}

/// Find the next available slot index (0-15). Returns None if all 16 are occupied.
pub fn next_slot_index(slots: &[ConnectSlot]) -> Option<u8> {
    for i in 0..MAX_CONNECT_SLOTS {
        if !slots.iter().any(|s| s.slot_index == i) {
            return Some(i);
        }
    }
    None
}

/// Create a redacted copy of a slot (secret replaced with empty string).
pub fn redact_slot(slot: &ConnectSlot) -> ConnectSlot {
    ConnectSlot {
        secret: String::new(),
        ..slot.clone()
    }
}

/// Constant-time string equality (prevents timing attacks on secret comparison).
fn constant_time_eq_str(a: &str, b: &str) -> bool {
    let a = a.as_bytes();
    let b = b.as_bytes();
    if a.len() != b.len() {
        return false;
    }
    let mut diff = 0u8;
    for (x, y) in a.iter().zip(b.iter()) {
        diff |= x ^ y;
    }
    diff == 0
}

