// Client approval policy types shared between firmware and bridge.
#[allow(unused_imports)]
use alloc::{format, string::{String, ToString}, vec, vec::Vec};


use serde::{Deserialize, Serialize};

/// Maximum connection slots per master.
pub const MAX_CONNECT_SLOTS: u8 = 16;

/// Maximum client pubkeys retained per slot. A slot's secret is a shared
/// credential, so every device that pairs with it is remembered and stays
/// auto-approved. Beyond this cap the oldest entry is dropped (FIFO). Keep
/// this modest: the persisted blob for all 16 slots must fit the NVS read
/// buffer in the firmware's `load_from_nvs`.
pub const MAX_AUTHORIZED_PUBKEYS: usize = 8;

/// A named connection slot for a specific master.
///
/// Each slot has its own bunker URI secret. The slot -- not the ephemeral
/// client pubkey -- is the stable identity. Every client that connects with
/// the slot secret is remembered in `authorized_pubkeys` and stays auto-approved;
/// clients are never evicted by a later pairing, so multiple devices sharing one
/// slot (e.g. signet-app on a phone plus a desktop) all sign without prompting.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ConnectSlot {
    /// Slot index (0-15).
    pub slot_index: u8,
    /// Human-readable label (e.g. "nostrudel desktop").
    pub label: String,
    /// Hex-encoded 32-byte secret (64 chars). Generated by hardware RNG.
    pub secret: String,
    /// Hex-encoded most-recently-connected client pubkey. Always also present
    /// in `authorized_pubkeys`; kept separately for display ("who's active").
    #[serde(default)]
    pub current_pubkey: Option<String>,
    /// Which NIP-46 methods are auto-approved for this slot.
    #[serde(default)]
    pub allowed_methods: Vec<String>,
    /// Which event kinds are auto-approved for sign_event.
    #[serde(default)]
    pub allowed_kinds: Vec<u64>,
    /// Whether to auto-approve matching requests (true) or just OLED-notify (false).
    #[serde(default)]
    pub auto_approve: bool,
    /// True once the user has physically approved a sign_event via button press.
    #[serde(default)]
    pub signing_approved: bool,
    /// Every client pubkey that has paired with this slot's secret. All are
    /// auto-approvable (subject to the slot's other gates). Capped at
    /// `MAX_AUTHORIZED_PUBKEYS`. Old NVS blobs without this field deserialise
    /// to an empty vec; `current_pubkey` still matches via [`slot_authorizes`],
    /// so an already-bound client keeps working until it next re-pairs.
    #[serde(default)]
    pub authorized_pubkeys: Vec<String>,
}

/// A client's approval policy for a specific master.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ClientPolicy {
    /// Hex-encoded client public key (64 chars).
    pub client_pubkey: String,
    /// Human-readable label (e.g. "Bark browser").
    #[serde(default)]
    pub label: String,
    /// Which NIP-46 methods are auto-approved.
    #[serde(default)]
    pub allowed_methods: Vec<String>,
    /// Which event kinds are auto-approved for sign_event.
    /// Empty = all kinds allowed.
    #[serde(default)]
    pub allowed_kinds: Vec<u64>,
    /// Whether to auto-approve matching requests (true) or just OLED-notify (false).
    #[serde(default)]
    pub auto_approve: bool,
}

/// Methods auto-approved on connect when the client provides a valid secret.
/// These are non-signing operations -- safe to grant without physical approval.
pub const CONNECT_SAFE_METHODS: &[&str] = &[
    "nip44_encrypt",
    "nip44_decrypt",
    "nip04_encrypt",
    "nip04_decrypt",
    "get_public_key",
];

/// Methods auto-approved after first physical button approval.
/// Includes signing -- only granted after the user explicitly approves once.
pub const TOFU_SAFE_METHODS: &[&str] = &[
    "sign_event",
    "nip44_encrypt",
    "nip44_decrypt",
    "nip04_encrypt",
    "nip04_decrypt",
    "get_public_key",
];

/// Approval decision for a specific request.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ApprovalTier {
    /// Proceed immediately without user interaction.
    AutoApprove,
    /// Show briefly on OLED (1s) but don't wait for button.
    OledNotify,
    /// Full OLED display with countdown bar, wait for button approval.
    ButtonRequired,
}

// ---------------------------------------------------------------------------
// Pure policy helpers (testable on host, used by firmware PolicyEngine)
// ---------------------------------------------------------------------------

/// Create a connect-time TOFU policy (no signing -- only granted on valid secret).
pub fn make_connect_policy(client_pubkey: &str) -> ClientPolicy {
    ClientPolicy {
        client_pubkey: client_pubkey.to_string(),
        label: String::new(),
        allowed_methods: CONNECT_SAFE_METHODS.iter().map(|s| s.to_string()).collect(),
        allowed_kinds: vec![],
        auto_approve: true,
    }
}

/// Create a full TOFU policy for a client after first physical button approval.
/// Includes sign_event -- only call this after the user has pressed the button.
pub fn make_tofu_policy(client_pubkey: &str) -> ClientPolicy {
    ClientPolicy {
        client_pubkey: client_pubkey.to_string(),
        label: String::new(),
        allowed_methods: TOFU_SAFE_METHODS.iter().map(|s| s.to_string()).collect(),
        allowed_kinds: vec![],
        auto_approve: true,
    }
}

/// Find a policy by client pubkey.
pub fn find_policy<'a>(policies: &'a [ClientPolicy], client_pubkey: &str) -> Option<&'a ClientPolicy> {
    policies.iter().find(|p| p.client_pubkey == client_pubkey)
}

/// Remove a client policy by pubkey. Returns true if found and removed.
pub fn revoke_policy(policies: &mut Vec<ClientPolicy>, client_pubkey: &str) -> bool {
    let before = policies.len();
    policies.retain(|p| p.client_pubkey != client_pubkey);
    policies.len() < before
}

/// Upsert a client policy. Replaces if client_pubkey matches, otherwise adds.
pub fn upsert_policy(policies: &mut Vec<ClientPolicy>, policy: ClientPolicy) {
    if let Some(existing) = policies.iter_mut().find(|p| p.client_pubkey == policy.client_pubkey) {
        *existing = policy;
    } else {
        policies.push(policy);
    }
}

